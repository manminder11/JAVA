public class VARIABLES {
    public static void main(String[] args) {

        int x; // declaring a variable
        x = 5; // assigning a value to a variable
        System.out.println(x); // printing the value of a variable


        // using the String literal
        String name = "John";
        System.out.println("name of the person is "+name);

        // work with large numbers
        long y = 1000000000000000000L;
        System.out.println(y);

        // WE CANNOT DO THE VICE VERSA IN LONG AND INT
        //  z = 1000000000000000000; // this will give an error
        // AS WELL AS WE CANNOT DO THE VICE VERSA IN INT AND LONG

        // FLOAT
        float f = 5.5f;
        System.out.println(f);

        // DOUBLE
        double d = 5.5;
        System.out.println(d);

        // BOOLEAN
        boolean b = true;
        System.out.println(b);

        // CHAR
        char c = 'A';
        System.out.println(c);

        // string
        String s = "Hello";
        System.out.println(s);



    }

    }

